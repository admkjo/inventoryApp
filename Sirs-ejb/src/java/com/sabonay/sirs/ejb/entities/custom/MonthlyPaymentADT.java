package com.sabonay.sirs.ejb.entities.custom;

import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author emma
 */
public class MonthlyPaymentADT implements Serializable{
    private static final long serialVersionUID = 1L;
    private String monthlyPeriod;
    private Date jobDate;
    private Double actualAmountDue;
    private Double discount;
    private Double nominalAmountDue;
    private Double amountPaid;
    private Double balance;

    public Date getJobDate() {
        return jobDate;
    }

    public void setJobDate(Date jobDate) {
        this.jobDate = jobDate;
    }

    public Double getActualAmountDue() {
        return actualAmountDue;
    }

    public void setActualAmountDue(Double actualAmountDue) {
        this.actualAmountDue = actualAmountDue;
    }

    public Double getDiscount() {
        return discount;
    }

    public void setDiscount(Double discount) {
        this.discount = discount;
    }

    public Double getNominalAmountDue() {
        return nominalAmountDue;
    }

    public void setNominalAmountDue(Double nominalAmountDue) {
        this.nominalAmountDue = nominalAmountDue;
    }

    public Double getAmountPaid() {
        return amountPaid;
    }

    public void setAmountPaid(Double amountPaid) {
        this.amountPaid = amountPaid;
    }

    public Double getBalance() {
        return balance;
    }

    public void setBalance(Double balance) {
        this.balance = balance;
    }

    public String getMonthlyPeriod() {
        return monthlyPeriod;
    }

    public void setMonthlyPeriod(String monthlyPeriod) {
        this.monthlyPeriod = monthlyPeriod;
    }
}
